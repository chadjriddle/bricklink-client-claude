namespace BrickLink.Client.Enums;

/// <summary>
/// Represents how a part appears in a superset.
/// </summary>
public enum AppearAs
{
    /// <summary>
    /// Alternate - The part is an alternate version in the superset.
    /// </summary>
    Alternate,

    /// <summary>
    /// Counterpart - The part is a counterpart in the superset.
    /// </summary>
    Counterpart,

    /// <summary>
    /// Extra - The part is an extra in the superset.
    /// </summary>
    Extra,

    /// <summary>
    /// Regular - The part appears normally in the superset.
    /// </summary>
    Regular
}
